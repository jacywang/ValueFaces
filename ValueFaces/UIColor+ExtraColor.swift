import UIKit

extension UIColor {
    class func manicCravingColor() -> UIColor {
        return UIColor(red: 245/255.0, green: 99/255.0, blue: 74/255.0, alpha: 1.0)
    }
    
    class func fontLightDarkColor() -> UIColor {
        return UIColor(red: 51/255.0, green: 51/255.0, blue: 51/255.0, alpha: 1.0)
    }
}
